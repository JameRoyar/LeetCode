# 这是一个包含多种语言元素的示例程序，包括字符串、注释、变量、循环、算术运算、函数调用等。下面是加上中文注释后的代码：
print("Hello world") # 输出字符串“Hello world”
sum1 = 0 # 定义变量sum，并将其初始化为0
for i in range(101): # 使用range函数生成一个从0到100的整数序列
    sum1 += i # 将当前整数加到sum中
print(sum1)

a = ("asd as da  asd \
    121312312") # 定义一个字符串，其中包含换行符和制表符
print(a)

c = 9.5 # 定义一个浮点数变量c，并将其赋值为9.5
d = round(c) # 使用round函数将c四舍五入为最接近的整数
d *= d+2 # 将d赋值为d的平方加2
print(d)
